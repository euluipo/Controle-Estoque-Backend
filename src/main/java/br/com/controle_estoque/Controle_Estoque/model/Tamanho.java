package br.com.controle_estoque.Controle_Estoque.model;

/**
 * Enumeração que define os tamanhos (dimensões ou porções)
 * que um produto ou categoria pode ter.
 */
public enum Tamanho {
    /**
     * Tamanho pequeno.
     */
    Pequeno,

    /**
     * Tamanho médio.
     */
    Médio,

    /**
     * Tamanho grande.
     */
    Grande
}